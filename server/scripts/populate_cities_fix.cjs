async function fixCities() {
    const key = 'cities_by_governorate';

    // First, fetch existing map to merge with (to preserve any custom cities added by user)
    db.get("SELECT value FROM kv_store WHERE `key` = ?", [key], (err, row) => {
        let currentMap = {};
        if (row) {
            try {
                currentMap = JSON.parse(row.value);
            } catch (e) {
                console.error("Error parsing current cities map", e);
            }
        }

        // Merge: for each gov in defaults, if current is empty, use default.
        // If current has cities, we could merge them or just overwrite if it was empty.
        // User said "أنا بردو عندى اماكنية اضيف مدن تانى", so I should preserve his custom ones.

        const mergedMap = { ...currentMap };

        for (const [gov, cities] of Object.entries(defaultCitiesMap)) {
            if (!mergedMap[gov] || mergedMap[gov].length === 0) {
                mergedMap[gov] = cities;
            } else {
                // Merge and remove duplicates
                const combined = Array.from(new Set([...cities, ...mergedMap[gov]]));
                mergedMap[gov] = combined;
            }
        }

        const replaceSql = "REPLACE INTO kv_store (`key`, value) VALUES (?, ?)";
        db.run(replaceSql, [key, JSON.stringify(mergedMap)], function (err) {
            if (err) {
                console.error("Error updating KV store:", err.message);
            } else {
                console.log("Successfully updated cities_by_governorate in KV store.");
            }
            process.exit(0);
        });
    });
}

